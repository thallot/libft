#include "libft.h"

void	ft_putnbr(int i)
{
	unsigned int z;

	if (i < 0)
	{
		ft_putchar('-');
		z = -i;
	}
	else
		z = i;
	if (z >= 10)
		ft_putnbr(z / 10);
	ft_putchar((z % 10) + 48);
}
