#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int i;
	int size;

	i = 0;
	if (s && f)
	{
		size = ft_strlen(s);
		while (i < size)
		{
			(*f)(&s[i]);
			i++;
		}
	}
}
